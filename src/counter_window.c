#include "game.h"

void counter_window(int counter, WINDOW *counter_wind) {
    int *arr = get_arr_counter(counter);
    char *numbers[][6] = {
        {" ___  ", 
         "/   \\ ",
         "| | | ",
         "\\___/ "},
        {" __   ",
         "/  |  ",
         " | |  ",
         "(___) "},
        {" ___  ",
         "(__ \\ ",
         " / _/ ",
         "(____)"},
        {" ___  ",
         "(__ ) ",
         " (_ \\ ",
         "(___/ "},
        {"  __  ",
         " /. | ",
         "(_  _)",
         "  (_| "},
        {" ___  ",
         "| __) ",
         "|__ \\ ",
         "(___/ "},
        {"  _   ",
         " / )  ",
         "/ _ \\ ",
         "\\___/ "},
        {" ___  ",
         "(__ ) ",
         " / /  ",
         "(_/   "},
        {" ___  ",
         "( _ ) ",
         "/ _ \\ ",
         "\\___/ "},
        {" ___  ",
         "/ _ \\ ",
         "\\_  / ",
         " (_/  "}
    };
    for (int j = 0; j < 3; j++) {
        for (int k = 0; k < 4; k++) {
            mvwprintw(counter_wind, k, 6 * j, "%s", numbers[arr[j]][k]);
        }
    }
}
